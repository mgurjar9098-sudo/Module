# • Write a Python program to import the math module and use functions like sqrt(), ceil(), 
# floor(). 

import math


print("math module")
print(math.sqrt(16))
print(math.ceil(33.3))
print(math.floor(33.3))




# • Write a Python program to generate random numbers using the random module. 

import random

print("random module")

print(random.random())
print(random.randint(1,100))
print(random.randrange(1,4))
print(random.choice(["mango","banana","papaya"]))

