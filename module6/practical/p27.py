# Write a Python program that manipulates and prints strings using various string methods. 

str="Welcome to python programming"

print(len(str))
print(str.upper())
print(str.lower())
print(str.capitalize())
print(str.title())
print(str.find('W'))
print(str.replace("Welcome","learning"))
print(str.index("to"))
print(str.startswith("Welcome"))
print(str.endswith('programming'))
print(str.count('o'))
print(str.isalpha())
print(str.isalnum())
print(str.isdigit())
print(','.join(str))
print(str.split(','))
print(str.zfill(40))
print(str.strip())