# Practical Example 5: Write a Python program to find greater and less than a number using 
# if_else. 

num1=25
num2=200

print("find the the great number ")
if(num1>num2):
    print("num1 is greater ",num1)
else:
    print("num2 is greater ",num2)


print("find the the less number ")
if(num1<num2):
    print("num1 is less ",num1)
else:
    print("num2 is less ",num2)


